while(True):
    numMes=int(input("Informe o número correspondente ao mês: "))
    print("Digite 0 para sair do programa.")
    if(numMes==0):
        print("Saindo...")
        break
    elif(numMes==1):
        print("Janeiro.")
    elif(numMes==2):
        print("Fevereiro.")
    elif(numMes==4):
        print("Abril.")
    elif(numMes==5):
        print("Maio.")
    elif(numMes==6):
        print("Junho.")
    elif(numMes==7):
        print("Julho.")
    elif(numMes==8):
        print("Agosto.")
    elif(numMes==9):
        print("Setembri.")
    elif(numMes==10):
        print("Outubro.")
    elif(numMes==11):
        print("Novembro.")
    elif(numMes==12):
        print("Dezembro.")
    else:
        print("Opção Inválida!")